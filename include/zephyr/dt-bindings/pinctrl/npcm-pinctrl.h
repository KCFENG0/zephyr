/* NPCM pin control offset calculation */
#define NPCM_PINCTRL_GROUP_SHIFT         (3)
#define NPCM_PINCTRL_GROUP_MASK          (0xFF)
#define NPCM_PINCTRL_GROUP_OFFSET(N)     (((N) & NPCM_PINCTRL_GROUP_MASK) << NPCM_PINCTRL_GROUP_SHIFT)
#define NPCM_PINCTRL_GROUP_BIT_MASK      ((1 << NPCM_PINCTRL_GROUP_SHIFT) - 1)
#define NPCM_PINCTRL_GROUP_BIT_OFFSET(N) ((N) & NPCM_PINCTRL_GROUP_BIT_MASK)
#define NPCM_PINCTRL_GROUP_IDX(group, bit)                                                         \
	(NPCM_PINCTRL_GROUP_OFFSET(group) + NPCM_PINCTRL_GROUP_BIT_OFFSET(bit))

#define NPCM_PINCTRL_PUPD_SHIFT         (3)
#define NPCM_PINCTRL_PUPD_MASK          (0xFF)
#define NPCM_PINCTRL_PUPD_OFFSET(N)     (((N) & NPCM_PINCTRL_PUPD_MASK) << NPCM_PINCTRL_PUPD_SHIFT)
#define NPCM_PINCTRL_PUPD_BIT_MASK      ((1 << NPCM_PINCTRL_PUPD_SHIFT) - 1)
#define NPCM_PINCTRL_PUPD_BIT_OFFSET(N) ((N) & NPCM_PINCTRL_PUPD_BIT_MASK)
#define NPCM_PINCTRL_PUPD_IDX(pupd, bit)                                                           \
	(NPCM_PINCTRL_PUPD_OFFSET(pupd) + NPCM_PINCTRL_PUPD_BIT_OFFSET(bit))

